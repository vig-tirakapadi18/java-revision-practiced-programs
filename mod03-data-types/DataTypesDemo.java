class DataTypesDemo {
    public static void main(String[] args) {
        byte age = 22;
        short salary = 30000;
        int pin_code = 587301;
        long ph_num = 9876543210L;

        System.out.println("Byte Data Type: "+age);
        System.out.println("Salary Data Type: "+salary);
        System.out.println("Integer Data Type: "+pin_code);
        System.out.println("Long Data Type: "+ph_num);
    }
}